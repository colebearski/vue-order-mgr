<template>
    <div>
        <h3>Todos</h3>
        <div class="todos">
            <div v-for="todo in allTodos" v-bind:key="todo.id" class="todo">
                {{ todo.title }}
            </div>
        </div>
    </div>
</template>

<script>
import { mapGetters, mapActions } from "vuex";

export default {
  name: "Todos",
  // for the Getters
  computed: mapGetters(["allTodos"]),
  // for the Actions
  methods: {
    ...mapActions(["getTodos"])
  },
  // Lifecycle method
  created() {
    this.getTodos();
  }
};
</script>

